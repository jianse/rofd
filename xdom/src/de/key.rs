use crate::de::XmlDeError;
use serde::de::Visitor;
use serde::Deserializer;

enum NameType {
    Attr,
    Ele,
    Text,
}

pub(super) struct KeyDe<'de> {
    name: &'de str,
    name_type: NameType,
}

impl<'de> KeyDe<'de> {
    pub(super) fn new_attr(name: &'de str) -> KeyDe<'de> {
        KeyDe {
            name,
            name_type: NameType::Attr,
        }
    }
    pub(super) fn new_ele(name: &'de str) -> KeyDe<'de> {
        KeyDe {
            name,
            name_type: NameType::Ele,
        }
    }

    pub(super) fn new_text() -> KeyDe<'de> {
        KeyDe {
            name: "$text",
            name_type: NameType::Text,
        }
    }
}

